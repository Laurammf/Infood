    <p class="lead-recipe">INGREDIENTS:
    <br>150g plain flour
    <br>1 tbsp golden caster sugar
    <br>large pinch bicarbonate of soda
    <br>½ tsp cinnamon
    <br>1 tsp cocoa powder (optional)
    <br>30g butter
    <br>1 egg, separated
    <br>50ml dry marsala or white wine
    <br>rapeseed oil
    <br>50g dark chocolate, melted
    <br>handful pistachio kernels, finely chopped
    <br>icing sugar, to dust
    <br><br>
    For the filling:
    <br>250g ricotta, drained and beaten until fluffy
    <br>100g mascarpone
    <br>2 tbsp finely chopped candied peel
    <br>2 tbsp icing sugar
</div>
