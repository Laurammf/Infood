           In a jug mix together the warm water and yeast. Leave for 15 minutes or until a light foam lays across the top of the water. If the foam doesn't appear your yeast may be too old, or your water too hot or cold. Tip away and try again.
        <br>In an electric mixer, fitted with a dough hook, mix the flour, salt and honey together. Whisk the yeasted water together one last time and pour into the mixer. Mix on low for 4 or 5 minutes, until the dough has come together into one lump.
        <br>Turn up the mixer and knead for another 10 minutes or until the dough is stiff and smooth. This is a much firmer dough than say bread dough. Scrape out of the bowl onto a surface lightly dusted with flour. Divide into eight equal portions and roll each piece into a ball.
        <br>Dust a couple of baking sheets with cornmeal (polenta). Next take each ball of dough and stick the handle of a wooden spoon through the centre. Carefully swing the dough around the handle to stretch the hole, and make the bagel shape. Stand each bagel on the cornmeal with an of inch or two of room around for them to spread.
        <br>Cover with cling film, and let sit on the kitchen counter for 2-3 hours, or overnight in the fridge.
        Bring a pan of water to the boil. The water needs to be 3-4 inches deep and the wider the pan the bette.
