        <div class="card-body">
        <p id="recipe-paragraph"> 
            Proof the yeast by dissolving it in 1/4 cup warm water and sugar for 5 minutes.Measure the flour into a mixing bowl, add the salt, the proofed yeast, and 1 cup of warm water. Mix by hand or with a stand mixer with a dough hook attachment until flour mixture comes together into a stiff ball. Add more warm water as needed to form the dough.
<br><br>
            Knead for 5 minutes and let rest for a few minutes. Add the butter and knead for at least 5 more minutes or until butter is fully incorporated. At this time, the dough should be firm and velvety to the touch.
<br><br>
            Form into a ball, butter all surfaces, and let rise until double, about 1 hour, in a warm spot. Shape the Pretzels
            Place wax paper on a baking sheet. De-gas (punch down) the dough. Divide it into 12 (2-ounce) pieces. Form the dough into balls.Using very little flour, form balls into 1-foot long strands, thicker in the middle and tapering towards the ends. 
<br><br>
            Take each strand and roll out again to form 2-foot strands. Twist into a pretzel shape, using a little water to make the ends stick to the loop. Experienced pretzel bakers can flip pretzels into shape in the air, but most people have to coax them. Place the pretzels on the baking sheet and refrigerate for 1 hour. This dries out the surface and makes them easier to handle. Make the Lye Solution
<br><br>
            Place 1 quart of water in a plastic or glass container, weigh 1 ounce of food grade or reagent grade sodium hydroxide into a bowl, and add slowly to the water, stirring with a plastic spoon or similar object. ALWAYS ADD THE LYE TO THE WATER, not the other way around! For scientists: The lye solution will be approximately 0.75 M NaOH (FW 39.99g/mol) or almost 3 percent w/w.
<br><br>
            Remove pretzels from the refrigerator and dip each for 30 seconds in the lye solution. Remove with a slotted spoon and place on a greased or parchment-paper-lined baking sheet.
<br><br>

            Sprinkle pretzel with salt. Make a deep cut through the thick part of the pretzel horizontally with a razor blade or lame. Let pretzels rest for 15 minutes. Sprinkle with salt Heat oven to 375 F. Bake pretzels for 20 to 25 minutes or until deep golden brown.
            
