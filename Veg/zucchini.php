    <div class="card bg-light text-dark" >
        <div class="card-body">
        <p id="recipe-paragraph"> Rinse lentils, then soak in 2 cups water at room temperature at least 1 hour and up to 12. Soaking the lentils for this long will make them super-plump and tender, which in turn makes them a lot easier to blend.
           <br><br>Meanwhile, trim ends of zucchini and cut crosswise into 3 pieces about 2" long. Cut each piece lengthwise into ¼"-thick planks. Stack a few planks and slice lengthwise into ¼"-thick matchsticks (about the size of a skinny french fry). Transfer zucchini to a colander set in a medium bowl. Add onion and 1 tsp. salt to colander and toss to combine. Let zucchini and onion sit until about 1 Tbsp. liquid has released and the vegetables look wilted and soft, 30 minutes–2 hours (to maximize your downtime, do this while the lentils are soaking). Gently pat dry with paper towels to remove any excess moisture. The less moisture in the veggies, the less soggy your fritters will be, so be sure not to skip this step.
             <br><br>Drain lentils and transfer to a food processor. Add chile powder, turmeric, and ¾ tsp. salt. Pulse, scraping down sides, until a purée forms. Transfer to a medium bowl and add zucchini and onion mixture, parsley, and lemon zest. Toss well with a rubber spatula to combine (it will look like too much vegetables for the lentils, but that’s just what you want).
               <br><br>Heat ghee in a 10" cast-iron skillet over medium-high until a small amount of batter added to the fat excitedly sizzles and sputters. Carefully drop ¼ cup batter into ghee, then flatten to a pancake with the back of a large metal spoon dipped into the hot ghee to prevent sticking. Repeat 3 times for a total of 4 fritters. Fry until deep golden brown on one side (look for the color of a well-fried falafel), about 3 minutes. Turn with a slotted spatula or large spoon and continue to fry until deep golden brown on the second side, about 3 minutes more. Transfer fritters to a wire rack set in a paper towel-lined baking sheet to drain. Season immediately with salt. Repeat with remaining batter (you should have 8–10 fritters).
                   <br><br>Transfer fritters to a platter. Serve hot with lemony yogurt alongside.
                     </p>  
